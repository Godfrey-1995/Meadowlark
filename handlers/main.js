/*====================================================================================================*/
exports.Headers = function(req, res) {
    let headers_content = '';
    for (let name in req.headers) {
        headers_content += name + ':' + req.headers[name] + '\n';
    }
    res.render('headers', {headers_content: headers_content});
}

